#include <unistd.h>

int    ft_atoi(char *str)
{
    int i;
    int    result;
    int    sign;
    
    i = 0;
    sign = 0;
    result = 0;
    while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' ||
           str[i] == '\v' || str[i] == '\r' || str[i] == '\f')
        i++;
    while (str[i] == '-' || str[i] == '+')
    {
        if (str[i] == '-')
            sign++;
        i++;
    }
    while (str[i] >= '0' && str[i] <= '9')
    {
        result = (result * 10) + (str[i] - '0');
        i++;
    }
    if (sign % 2 != 0)
        result = -result;
    return (result);
}

