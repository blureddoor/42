char    *ft_strstr(char *str, char *to_find)
{
    unsigned int    i;
    unsigned int    j;
    
    if (!*to_find)
        return ((char*)str);
    i = 0;
    while (str[i] != '\0')
    {
        if (str[i] == to_find[0])
        {
            j = 0;
            while (to_find[j] != '\0' && str[i + j] == to_find[j])
            {
                j++;
            }
            if (to_find[j] == '\0')
                return (&str[i]);
            else
                return (NULL);
        }
        i++;
    }
    return (0);
}

