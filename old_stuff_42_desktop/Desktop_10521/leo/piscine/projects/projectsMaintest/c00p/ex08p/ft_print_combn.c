#include <unistd.h>
#include <math.h>

void	ft_print_combn(int n)
{
	int m;

	m = 0 + '0';
	while (m <= '5')
	{
		while (m <= '9')
		{
		write(1, &m, 1);
		write(1, " ", 1);
		m++;
		}
	write(1, "A", 1);
	write(1, &m, 1);
	m++;
	}
}
