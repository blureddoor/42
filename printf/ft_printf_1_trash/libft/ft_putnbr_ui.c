void	ft_putnbr_ui(unsigned int nb)
{
	unsigned int     y;
	unsigned long    z;
	
	z = nb;

	if (z >= 10)
		ft_putnbr(z / 10);
	y = z % 10 + '0';
	write(1, &y, 1);
}
